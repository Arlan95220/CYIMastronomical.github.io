	function disp(id) {
	var ww = document.getElementById(id);
	if (ww) {
     ww.style.display = 'block';
	 }
} 
	function disn(id) {
	var wn = document.getElementById(id);
	if (wn) {
     wn.style.display = 'none';
	 }
} 
	function op(id) {
	var wr = document.getElementById(id);
	if (wr) {
     wr.style.opacity = 1;
	 }
} 
	function opn(id) {
	var wrr = document.getElementById(id);
	if (wrr) {
     wrr.style.opacity = 0;
	 }
	 }