
	function talking(id) {
	var talked = document.getElementById(id);
	var pop = document.getElementById('pop');
	pop.style.display = 'block';
    talked.style.display = 'block';
	} 
	function talknot(id) {
	var talkn = document.getElementById(id);
	var pop = document.getElementById('pop');
	pop.style.display = 'none';
    talkn.style.display = 'none';
	} 
